function parseTSV(tsv) {
  const lines = tsv
    .split(/\r?\n/)
    .map(l => l.trim())
    .filter(Boolean);
  const rows = [];
  for (const line of lines) {
    const cols = line.split(/\t+/); // 탭 기준 분리
    if (!cols.length) continue;

    if (cols.length >= 4) {
      const [name, address, phone, benefitsRaw, category] = cols;
      const benefits = (benefitsRaw || '')
        .split(/\s*[/•-]\s*|\s{2,}/)
        .map(s => s.trim())
        .filter(Boolean);
      
      // 카테고리가 없으면 기본값 '기타' 사용
      const finalCategory = (category && category.trim()) || '기타';
      
      rows.push({
        name,
        address,
        phone,
        benefits,
        category: finalCategory,
        lat: null,
        lng: null,
      });
      continue; 
    }

    // 그 외 케이스는 스킵
  }
  return rows;
}

